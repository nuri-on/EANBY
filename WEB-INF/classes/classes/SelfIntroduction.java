package classes;

public class SelfIntroduction {
	 private String ItemName;
	 private int IntroSize;  
	 private int SelfIntroNum;
	 private String selfIntroContents;
	 
	public String getItemName() {
		return ItemName;
	}
	public void setItemName(String itemName) {
		ItemName = itemName;
	}
	public int getIntroSize() {
		return IntroSize;
	}
	public void setIntroSize(int introSize) {
		IntroSize = introSize;
	}
	public int getSelfIntroNum() {
		return SelfIntroNum;
	}
	public void setSelfIntroNum(int selfIntroNum) {
		SelfIntroNum = selfIntroNum;
	}
	public String getSelfIntroContents() {
		return selfIntroContents;
	}
	public void setSelfIntroContents(String selfIntroContents) {
		this.selfIntroContents = selfIntroContents;
	}
	 
	 
	 
}
